public class IncrementoDecremento {
    public static void main(String[] args) {
        int numero = 5;
        
        numero = numero + 2;
        
        System.out.println(numero);

        int numeroDois = 2;

        numeroDois++;

        System.out.println(numeroDois);

        int numeroTres = 3;

        System.out.println(numeroTres --);
        System.out.println(numeroTres);
        /* Realizando o decremento após a execução do método de impressão.
        Também pode ser adicionado como prefixo.*/

    }
}
